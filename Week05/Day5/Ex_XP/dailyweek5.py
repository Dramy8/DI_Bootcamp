# Q5 — Pareto Principle: Customers And Profit
import numpy as np

customer_profit = (
    df.groupby("Customer Name")["Profit"]
    .sum()
    .sort_values(ascending=False)
)

profitable_customers = customer_profit[customer_profit > 0]

total_positive_profit = profitable_customers.sum()

cumulative_profit = profitable_customers.cumsum() / total_positive_profit * 100

customer_percentage = (
    np.arange(1, len(profitable_customers) + 1)
    / len(profitable_customers) * 100
)

plt.figure(figsize=(12, 7))

plt.plot(
    customer_percentage,
    cumulative_profit.values,
    color="#3498db",
    linewidth=2
)

plt.axhline(y=80, color="red", linestyle="--", label="80% of Profit")
plt.axvline(x=20, color="orange", linestyle="--", label="20% of Customers")

plt.title("Pareto Analysis: Customers and Profit")
plt.xlabel("Percentage of Profitable Customers")
plt.ylabel("Cumulative Profit (%)")
plt.legend()
plt.grid(True, alpha=0.3)
plt.show()

# The graph gives a visual indication of whether the Pareto principle applies.
# However, the numerical check confirms the result more precisely.

top_20_count = int(np.ceil(len(profitable_customers) * 0.20))

top_20_profit = profitable_customers.head(top_20_count).sum()
top_20_profit_percentage = top_20_profit / total_positive_profit * 100

print("Top 20% profitable customers contribute to", round(top_20_profit_percentage, 1), "% of positive profit")

