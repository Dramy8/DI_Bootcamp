# Q9 — Marketing Strategy Decisions
state_full = df.groupby("State").agg(
    Total_Sales=("Sales", "sum"),
    Total_Profit=("Profit", "sum"),
    Num_Orders=("Order ID", "nunique"),
    Num_Customers=("Customer ID", "nunique")
)

state_full["Profit_Margin_%"] = (
    state_full["Total_Profit"] / state_full["Total_Sales"] * 100
).round(2)

state_full["Avg_Order_Value"] = (
    state_full["Total_Sales"] / state_full["Num_Orders"]
).round(2)

plt.figure(figsize=(14, 8))

scatter = plt.scatter(
    state_full["Total_Sales"],
    state_full["Total_Profit"],
    s=state_full["Num_Customers"] * 5,
    c=state_full["Profit_Margin_%"],
    cmap="RdYlGn",
    alpha=0.6,
    edgecolor="black"
)

plt.colorbar(scatter, label="Profit Margin (%)")
plt.axhline(y=0, color="red", linestyle="--")

plt.xlabel("Total Sales")
plt.ylabel("Total Profit")
plt.title("States: Sales vs Profit")

for state in ["California", "New York", "Texas", "Washington", "Pennsylvania"]:
    if state in state_full.index:
        row = state_full.loc[state]
        plt.annotate(
            state,
            (row["Total_Sales"], row["Total_Profit"]),
            fontsize=9,
            fontweight="bold"
        )

plt.tight_layout()
plt.show()

# Based on the analysis, marketing should prioritize states and cities with both high sales and high profit.
# High-sales but low-profit areas should be reviewed carefully,
